<script setup lang="ts">
import { ref, computed, watch, onMounted, onUnmounted, defineProps, defineEmits } from 'vue';
import { marked } from 'marked';

const props = defineProps({
  modelValue: {
    type: String,
    default: ''
  },
  title: {
    type: String,
    default: 'Markdown È¢ÑËßà'
  }
});

const emit = defineEmits(['update:modelValue']);

const localContent = ref(props.modelValue);
const isEditMode = ref(false);
const copyStatus = ref('');
const showModelDropdown = ref(false);

// ÂèØÁî®ÁöÑÂ§ßÊ®°ÂûãÂàóË°®
const availableModels = [
  {
    name: 'DeepSeek',
    url: 'https://chat.deepseek.com/',
    promptParam: 'prompt'
  },
  { 
    name: 'ChatGPT', 
    url: 'https://chat.openai.com/',
    promptParam: 'prompt'
  },
  { 
    name: 'Claude', 
    url: 'https://claude.ai/chats',
    promptParam: 'text'
  },
  {
    name: 'Gemini',
    url: 'https://gemini.google.com/',
    promptParam: 'prompt'
  },
  { 
    name: 'ÊñáÂøÉ‰∏ÄË®Ä', 
    url: 'https://yiyan.baidu.com/',
    promptParam: 'query'
  },
  { 
    name: 'ÈÄö‰πâÂçÉÈóÆ', 
    url: 'https://qianwen.aliyun.com/',
    promptParam: 'query'
  }
];

// ÁõëÂê¨ props ÂèòÂåñ
watch(() => props.modelValue, (newValue) => {
  localContent.value = newValue;
});

const renderedMarkdown = computed(() => {
  try {
    return marked(localContent.value);
  } catch (error) {
    console.error('Markdown Ê∏≤ÊüìÈîôËØØ:', error);
    return '<div class="error">Markdown Ê∏≤ÊüìÈîôËØØ</div>';
  }
});

const updateContent = () => {
  emit('update:modelValue', localContent.value);
};

const toggleEditMode = () => {
  isEditMode.value = !isEditMode.value;
};

const copyContent = async () => {
  try {
    await navigator.clipboard.writeText(localContent.value);
    copyStatus.value = 'Â∑≤Â§çÂà∂Âà∞Ââ™Ë¥¥ÊùøÔºÅ';
    setTimeout(() => {
      copyStatus.value = '';
    }, 2000);
  } catch (error) {
    console.error('Â§çÂà∂Â§±Ë¥•:', error);
    copyStatus.value = 'Â§çÂà∂Â§±Ë¥•ÔºåËØ∑ÊâãÂä®Â§çÂà∂';
    setTimeout(() => {
      copyStatus.value = '';
    }, 2000);
  }
};

// ÂàáÊç¢Ê®°Âûã‰∏ãÊãâËèúÂçï
const toggleModelDropdown = () => {
  showModelDropdown.value = !showModelDropdown.value;
};

// ÁÇπÂáªÂ§ñÈÉ®ÂÖ≥Èó≠‰∏ãÊãâËèúÂçï
const closeDropdownOnClickOutside = (event: MouseEvent) => {
  const dropdown = document.querySelector('.dropdown');
  if (dropdown && !dropdown.contains(event.target as Node) && showModelDropdown.value) {
    showModelDropdown.value = false;
  }
};

const sendToModel = (model: any) => {
  
  let url = model.url;
  url = `${model.url}`;
  copyContent();
  copyStatus.value = 'Â∑≤Â§çÂà∂Âà∞Ââ™Ë¥¥ÊùøÔºÅËØ∑Âú®Ê®°Âûã‰∏≠Á≤òË¥¥';
  window.open(url, '_blank');
  
  showModelDropdown.value = false;
};

const handleResize = () => {
};

// ÂàùÂßãÂåñ
onMounted(() => {
  window.addEventListener('resize', handleResize);
  document.addEventListener('click', closeDropdownOnClickOutside);
});

onUnmounted(() => {
  window.removeEventListener('resize', handleResize);
  document.removeEventListener('click', closeDropdownOnClickOutside);
});
</script>

